# ── Helper functions ───────────────────────────────────────────────────────
def _streak(results: list) -> str:
    if not results:
        return "-"
    current = results[-1]
    count = 0
    for r in reversed(results):
        if r == current:
            count += 1
        else:
            break
    return f"{'W' if current == 1 else 'L'}{count}"


def _trend(season_era: float, recent_era: float) -> str:
    delta = season_era - recent_era
    if delta > 0.5:
        return "hot"
    if delta < -0.5:
        return "cold"
    return "neutral"


