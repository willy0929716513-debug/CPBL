_NPB_CL = {"GNT", "HNS", "HRC", "YDB", "YKL", "CND"}
_NPB_PL = {"SBH", "ORX", "RKT", "LTT", "SEI", "HAM"}


def get_standings() -> list:
    rows = []
    for code, s in TEAM_STATS.items():
        r = s["record"]
        league = TEAM_INFO[code]["league"]
        if league == "NPB":
            division = "NPB 中央" if code in _NPB_CL else "NPB 太平洋"
        else:
            division = "KBO"
        rows.append({
            "code": code,
            "name": TEAM_INFO[code]["name"],
            "short": TEAM_INFO[code]["short"],
            "league": league,
            "division": division,
            "w": r["w"], "l": r["l"], "pct": r["pct"],
            "run_diff": r["run_diff"],
            "last5": r["last5"],
            "elo": s["elo"],
            "streak": _streak(r["last10"]),
        })
    rows.sort(key=lambda x: (-x["pct"], -x["run_diff"]))
    for i, row in enumerate(rows):
        row["rank"] = i + 1
    return rows


def get_top_pitchers(n: int = 10) -> list:
    pitchers = []
    for name, p in PITCHERS.items():
        if p.get("gs", 0) >= 5:
            pitchers.append({
                "name": name,
                "team": p["team"],
                "team_name": TEAM_INFO[p["team"]]["short"],
                "league": TEAM_INFO[p["team"]]["league"],
                "foreign": p["foreign"],
                "era": p["era"],
                "fip": p["fip"],
                "xfip": p["xfip"],
                "whip": p["whip"],
                "k9": p["k9"],
                "bb9": p["bb9"],
                "gs": p["gs"],
                "recent_5_era": p["recent_5_era"],
                "trend": _trend(p["era"], p["recent_5_era"]),
            })
    pitchers.sort(key=lambda x: x["era"])
    return pitchers[:n]
